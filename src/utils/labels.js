/**
 * Get a localised label for the given key and language.
 * Falls back to English if the language is not in the map.
 *
 * @param {string} key       - Label key (e.g. 'who_is_it_for')
 * @param {string} langCode  - BCP-47 code (e.g. 'gu-IN') or short code (e.g. 'gu')
 * @returns {string}         - Localised string
 */
export function getLocalLabel(key, langCode = 'en-IN') {
  const map = LABELS[key];
  if (!map) return key;   // Unknown key — return key itself as placeholder

  // Accept both short codes ('gu') and BCP-47 ('gu-IN')
  const bcp47 = langCode.includes('-') ? langCode : `${langCode}-IN`;

  return map[bcp47] || map['en-IN'] || key;
}
