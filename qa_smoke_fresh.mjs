import puppeteer from 'puppeteer';

const base = 'http://localhost:5175';

async function runCase(page, lang, query, expectedHints) {
  await page.goto(base + '/', { waitUntil: 'networkidle2' });
  await page.waitForSelector('#start-btn');
  await page.click('#start-btn');
  await page.waitForSelector('#type-option');
  await page.click('#type-option');
  await page.waitForSelector('#question-input');
  await page.select('#language-dropdown', lang);
  await page.$eval('#question-input', (el, val) => {
    el.value = val;
    el.dispatchEvent(new Event('input', { bubbles: true }));
  }, query);
  await page.click('#ask-btn');
  await page.waitForFunction(() => location.hash === '#explanation' || location.hash === '#clarification', { timeout: 45000 });
  const route = await page.evaluate(() => location.hash);
  const body = await page.evaluate(() => document.body.innerText);
  const hits = expectedHints.filter(t => body.includes(t));
  return { route, hits };
}

(async () => {
  const browser = await puppeteer.launch({ headless: 'new' });
  const page = await browser.newPage();
  page.setDefaultTimeout(30000);
  const out = { gu: null, hi: null, errors: [] };
  try {
    out.gu = await runCase(page, 'gu-IN', '???? ????? ???? ???? ???? ???? ?????', ['? ???? ???? ???', '????? ??? ???', '???? ???? ???? ????', '????? ?????????']);
    out.hi = await runCase(page, 'hi-IN', '???????????? ???? ????? ?? ??? ???? ????? ????', ['?? ????? ??? ???', '???? ??? ????', '????? ???? ????', '???? ?? ????????? ?????']);
  } catch (e) {
    out.errors.push(e.message);
  }
  console.log(JSON.stringify(out, null, 2));
  await browser.close();
})();
